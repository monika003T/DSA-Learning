import java.util.*;
public class Swap {
    public static void main(String[] args) {
        int a=5;
        int b=9;
        System.out.println("a is"+a);
        System.out.println("b is"+b);
        int temp=a;
        a=b;
        b=temp;
        System.out.println("a is"+a);
        System.out.println("b is"+b);

    }
}
